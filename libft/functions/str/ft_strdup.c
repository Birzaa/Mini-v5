#include "../includes/libft.h"

char	*ft_strdup(const char *s)
{
	size_t	len;
	char	*tab;
	int		i;

	len = ft_strlen(s);
	tab = malloc(sizeof(char) * len + 1);
	if (!tab)
		return (NULL);
	i = 0;
	while (s[i])
	{
		tab[i] = s[i];
		i++;
	}
	tab[i] = '\0';
	return (tab);
}
