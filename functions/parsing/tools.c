int	ft_isspace(char c)
{
	if (c >= 9 && c <= 13)
		return (1);
	if (c == 32)
		return (1);
	return (0);
}

int	is_charset(char c)
{
	if (c == '\'' || c == '\"' || c == '<' || c == '>' || c == '|' || c == '$'
		|| ft_isspace(c) || c == '\n' || c == '\0')
		return (1);
	return (0);
}
